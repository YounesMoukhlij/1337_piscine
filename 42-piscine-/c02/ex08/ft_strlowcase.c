#include <stdio.h>

char	*ft_strlowcase(char *str)
{
	int	n;

	n = 0;
	while (str[n])
	{
		if (str[n] >= 65 && str[n] <= 90)
			str[n] += 32;
		n++;
	}
	return (str);
}
